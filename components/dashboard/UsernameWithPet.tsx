// ─── Pet registry ─────────────────────────────────────────────────────────────

export type PetId = "none" | "cat" | "dog" | "frog" | "bunny" | "penguin";

interface BehaviorResult {
  frame: Frame;
  yOffset: number;
}

interface PetDef {
  label: string;
  colors: string[];
  shadowColor: string;
  speed: number;
  behavior: (t: number, x: number, maxX: number, dir: number) => BehaviorResult;
}

