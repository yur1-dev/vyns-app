// ─── Renderer ─────────────────────────────────────────────────────────────────

function renderFrame(
  ctx: CanvasRenderingContext2D,
  frame: Frame,
  colors: string[],
  flipped: boolean,
  yOffset: number,
) {
  ctx.clearRect(0, 0, SW, SH + 20);
  ctx.save();
  if (flipped) {
    ctx.translate(SW, 0);
    ctx.scale(-1, 1);
  }
  for (let row = 0; row < H; row++) {
    for (let col = 0; col < W; col++) {
      const ci = frame[row][col];
      if (!ci) continue;
      ctx.fillStyle = colors[ci];
      ctx.fillRect(col * S, row * S + yOffset, S, S);
    }
  }
  ctx.restore();
}

// ─── PetCanvas ────────────────────────────────────────────────────────────────

interface PetCanvasProps {
  petId: Exclude<PetId, "none">;
  containerWidth: number;
  themeColor: string;
}

function PetCanvas({ petId, containerWidth, themeColor }: PetCanvasProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const rafRef = useRef<number>(0);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    ctx.imageSmoothingEnabled = false;

    const pet = PETS[petId];
    // Swap accent color (index 7) with user theme color for a bit of personality
    const colors = [...pet.colors];
    colors[7] = themeColor;

    const maxX = Math.max(0, containerWidth - SW);
    let x = Math.random() * maxX;
    let dir = Math.random() > 0.5 ? 1 : -1;
    // Frog/bunny: only move during hop arc
    let isFrogMoving = false;
    const startTime = performance.now();

    const loop = (now: number) => {
      const t = now - startTime;

      // Frog movement: only while airborne
      if (petId === "frog") {
        const hop = Math.floor(t / 60) % 22;
        isFrogMoving = hop >= 7 && hop < 13;
        if (isFrogMoving) x += pet.speed * dir;
      } else {
        x += pet.speed * dir;
      }

      // Bounce at edges
      if (x >= maxX) {
        x = maxX;
        dir = -1;
      }
      if (x <= 0) {
        x = 0;
        dir = 1;
      }

      const { frame, yOffset } = pet.behavior(t, x, maxX, dir);
      const flipped = dir === -1;

      canvas.style.transform = `translateX(${Math.round(x)}px)`;
      renderFrame(ctx, frame, colors, flipped, yOffset);

      rafRef.current = requestAnimationFrame(loop);
    };

    rafRef.current = requestAnimationFrame(loop);
    return () => cancelAnimationFrame(rafRef.current);
  }, [petId, containerWidth, themeColor]);

  return (
    <canvas
      ref={canvasRef}
      width={SW}
      height={SH + 20}
      style={{
        imageRendering: "pixelated",
        position: "absolute",
        top: 0,
        left: 0,
        willChange: "transform",
      }}
    />
  );
}

// ─── Public component ─────────────────────────────────────────────────────────

interface UsernameWithPetProps {
  username: string;
  petId: PetId;
  themeColor?: string;
  className?: string;
  textClassName?: string;
}

export default function UsernameWithPet({
  username,
  petId,
  themeColor = "#2dd4bf",
  className = "",
  textClassName = "text-sm font-medium text-white/80",
}: UsernameWithPetProps) {
  const containerRef = useRef<HTMLDivElement>(null);
  const [width, setWidth] = useState(160);

  useEffect(() => {
    const el = containerRef.current;
    if (!el) return;
    const ro = new ResizeObserver(([e]) => setWidth(e.contentRect.width));
    ro.observe(el);
    setWidth(el.offsetWidth);
    return () => ro.disconnect();
  }, []);

  const hasPet = petId !== "none" && petId in PETS;

  return (
    <div
      className={`inline-flex flex-col items-start ${className}`}
      style={{ minWidth: 0 }}
    >
      {hasPet && (
        <div
          ref={containerRef}
          style={{
            position: "relative",
            width: "100%",
            height: SH + 20,
            overflow: "visible",
            pointerEvents: "none",
          }}
        >
          <PetCanvas
            petId={petId as Exclude<PetId, "none">}
            containerWidth={width}
            themeColor={themeColor}
          />
        </div>
      )}
      <span
        className={`truncate max-w-full ${textClassName}`}
        style={{ lineHeight: 1.2 }}
      >
        {username}
      </span>
    </div>
  );
}
